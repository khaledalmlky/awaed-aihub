export function buildPromptWithContext(tool: keyof typeof TOOL_PROMPTS, knowledgeContext: string): string {
  let prompt = TOOL_PROMPTS[tool];
  
  if (knowledgeContext) {
    prompt += `

--- معلومات من قاعدة معرفة الفريق ---
${knowledgeContext}
--- نهاية معلومات قاعدة المعرفة ---

استخدم هذه المعلومات لتحسين إجاباتك وتخصيصها لأسلوب معيار عوائد.`;
  }
  
  return prompt;
}
